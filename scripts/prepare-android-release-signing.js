const fs = require('node:fs')
const path = require('node:path')
const { spawnSync } = require('node:child_process')

const ROOT = path.resolve(__dirname, '..')
const ANDROID_DIR = path.join(ROOT, 'android')
const APP_DIR = path.join(ANDROID_DIR, 'app')
const KEYSTORE_DIR = path.join(ANDROID_DIR, 'keystore')
const SIGNING_PROPS_PATH = path.join(ANDROID_DIR, 'release-signing.properties')

function ensureAndroidProject() {
  if (!fs.existsSync(APP_DIR)) {
    throw new Error('Android project not found. Run: npm run mobile:android:add')
  }
}

function parseProperties(content) {
  const out = {}
  for (const line of content.split(/\r?\n/)) {
    const trimmed = line.trim()
    if (!trimmed || trimmed.startsWith('#')) continue
    const i = trimmed.indexOf('=')
    if (i <= 0) continue
    const key = trimmed.slice(0, i).trim()
    const value = trimmed.slice(i + 1).trim()
    out[key] = value
  }
  return out
}

function resolveSigningConfig() {
  const existing = fs.existsSync(SIGNING_PROPS_PATH)
    ? parseProperties(fs.readFileSync(SIGNING_PROPS_PATH, 'utf8'))
    : {}

  const config = {
    storeFile:
      process.env.SHICI_ANDROID_STORE_FILE
      || existing.storeFile
      || 'keystore/release.keystore',
    storePassword:
      process.env.SHICI_ANDROID_STORE_PASSWORD
      || existing.storePassword
      || '',
    keyAlias:
      process.env.SHICI_ANDROID_KEY_ALIAS
      || existing.keyAlias
      || 'shici-zhaji',
    keyPassword:
      process.env.SHICI_ANDROID_KEY_PASSWORD
      || existing.keyPassword
      || process.env.SHICI_ANDROID_STORE_PASSWORD
      || existing.storePassword
      || '',
    dname:
      process.env.SHICI_ANDROID_DNAME
      || 'CN=shici-zhaji, OU=Mobile, O=yeebo, L=Shanghai, ST=Shanghai, C=CN',
  }

  for (const [key, value] of Object.entries(config)) {
    if (!value || !String(value).trim()) {
      throw new Error(`Missing signing value: ${key}`)
    }
  }

  return config
}

function toAbsoluteStorePath(storeFile) {
  const normalized = String(storeFile).trim()
  if (path.isAbsolute(normalized)) return normalized
  return path.join(ANDROID_DIR, normalized)
}

function ensureKeystore(config) {
  const keystorePath = toAbsoluteStorePath(config.storeFile)
  fs.mkdirSync(path.dirname(keystorePath), { recursive: true })

  if (fs.existsSync(keystorePath)) {
    return keystorePath
  }

  const args = [
    '-genkeypair',
    '-v',
    '-storetype', 'PKCS12',
    '-keystore', keystorePath,
    '-alias', config.keyAlias,
    '-keyalg', 'RSA',
    '-keysize', '2048',
    '-validity', '36500',
    '-storepass', config.storePassword,
    '-keypass', config.keyPassword,
    '-dname', config.dname,
  ]

  const result = spawnSync('keytool', args, { stdio: 'inherit' })
  if (result.status !== 0) {
    throw new Error(`keytool failed with code ${result.status || 1}`)
  }

  return keystorePath
}

function writeSigningProps(config) {
  fs.mkdirSync(KEYSTORE_DIR, { recursive: true })
  const body = [
    '# Auto-generated by scripts/prepare-android-release-signing.js',
    `storeFile=${config.storeFile}`,
    `storePassword=${config.storePassword}`,
    `keyAlias=${config.keyAlias}`,
    `keyPassword=${config.keyPassword}`,
    '',
  ].join('\n')
  fs.writeFileSync(SIGNING_PROPS_PATH, body, 'utf8')
}

function main() {
  ensureAndroidProject()
  const config = resolveSigningConfig()
  const keystorePath = ensureKeystore(config)
  writeSigningProps(config)
  console.log(`[android-signing] prepared release signing: ${SIGNING_PROPS_PATH}`)
  console.log(`[android-signing] keystore: ${keystorePath}`)
}

main()
